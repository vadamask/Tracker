import UIKit

extension UIColor {
    static var backgroundYP: UIColor { UIColor(named: "Background") ?? UIColor.gray }
    static var blackYP: UIColor { UIColor(named: "Black") ?? UIColor.black }
    static var blackYP30: UIColor { UIColor(named: "Black 30%") ?? UIColor.black }
    static var blueYP: UIColor { UIColor(named: "Blue") ?? UIColor.blue }
    static var grayYP: UIColor { UIColor(named: "Gray") ?? UIColor.gray }
    static var lightGrayYP: UIColor { UIColor(named: "Light Gray") ?? UIColor.lightGray }
    static var redYP: UIColor { UIColor(named: "Red") ?? UIColor.red }
    static var whiteYP: UIColor { UIColor(named: "White") ?? UIColor.white }
}
